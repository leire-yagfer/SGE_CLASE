#Ejemplo de uso de la librería estándar math para calcular la raíz cuadrada y el valor de pi.
# Importo la librería estándar math
import math

# Uso de la función sqrt() para obtener la raíz cuadrada de un número
raiz = math.sqrt(25)
print(f"La raíz cuadrada de 25 es: {raiz}")

# Uso de la constante pi
print(f"El valor de pi es: {math.pi}")


